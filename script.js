/**
 * Global function to set language
 */
window.setLanguage = function(lang) {
    document.documentElement.lang = lang;
    localStorage.setItem('preferredLang', lang);

    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });

    // Update active button state
    document.querySelectorAll('.lang-switch button').forEach(btn => {
        btn.classList.remove('active');
    });
    
    const activeBtn = document.getElementById(`lang-${lang}`);
    if (activeBtn) activeBtn.classList.add('active');
};

document.addEventListener('DOMContentLoaded', () => {
    // Initial Video Setup
    const heroVideo = document.getElementById('hero-video');
    if (heroVideo) {
        const videoId = heroVideo.dataset.videoId;
        const params = new URLSearchParams({
            autoplay: '1',
            mute: '1',
            loop: '1',
            playlist: videoId,
            controls: '0',
            rel: '0',
            playsinline: '1'
        });

        if (window.location.protocol === 'http:' || window.location.protocol === 'https:') {
            params.set('origin', window.location.origin);
        }

        heroVideo.src = `https://www.youtube-nocookie.com/embed/${videoId}?${params.toString()}`;
    }

    // Scroll Animations
    const observerOptions = { threshold: 0.1 };
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('active');
            }
        });
    }, observerOptions);

    const revealElements = document.querySelectorAll('.reveal');
    revealElements.forEach(el => observer.observe(el));

    // Language switch buttons event listeners
    const btnPl = document.getElementById('lang-pl');
    const btnEn = document.getElementById('lang-en');
    
    if (btnPl) btnPl.addEventListener('click', () => window.setLanguage('pl'));
    if (btnEn) btnEn.addEventListener('click', () => window.setLanguage('en'));

    // Language Initialization
    const savedLang = localStorage.getItem('preferredLang') || 'pl';
    window.setLanguage(savedLang);
});
